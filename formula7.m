% 公式(7)：生成复指数向量φ_q(g)
clear; clc;

%% 1. 自定义参数（可修改）
mu = 3;          % 向量g的长度参数（需与公式(6)一致）
q = 4;           % q次单位根参数（如q=2/4/8/16）
% 手动输入公式(6)生成的向量g（也可改为自动生成，见注释）
g = [0, 1, 2, 3, 4, 5, 6, 7];  

%% 2. 生成φ_q(g)
% 步骤1：计算q次单位根 ω_q = e^(-j*2π/q)（需正号可去掉"-"）
omega_q = exp(-1j * 2 * pi / q);

% 步骤2：逐元素计算ω_q^g_n（点运算保证逐元素处理）
phi_q_g = omega_q .^ g;

%% 3. 结果输出
disp('=== 公式(7) - 复指数向量φ_q(g)生成结果 ===');
disp(['参数μ = ', num2str(mu), ' → 向量长度 = 2^μ = ', num2str(2^mu)]);
disp(['参数q = ', num2str(q), ' → q次单位根 ω_', num2str(q), ' = ']);
disp(omega_q);
disp(['复指数向量φ_', num2str(q), '(g)（行向量，复值）：']);
disp(phi_q_g);

% 可选：验证单位根特性（所有元素模为1）
phi_mag = abs(phi_q_g);
disp('φ_q(g)各元素的模（验证单位根特性，应全为1）：');
disp(phi_mag);

